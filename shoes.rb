# shoe size checker
  # if your shoe size is less than 7 -> :( 
  # if your shoe size is between 7 && 9.5 -> "Yay! New shoes!"
  # if your shoe size is over 9.5 -> "booooo"
  
shoe_size = 10 

if shoe_size < 7
  puts ":("
 

elsif shoe_size >= 7 && shoe_size <= 9.5
puts "yay! new shoes!!"

else 
  puts "booo"
end 

